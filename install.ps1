# Colors
function Write-Info($message) { Write-Host "[INFO] $message" -ForegroundColor Green }
function Write-Warn($message) { Write-Host "[WARN] $message" -ForegroundColor Yellow }
function Write-Err($message) { Write-Host "[ERROR] $message" -ForegroundColor Red }

# Check if running as admin
function Test-Admin {
    $currentUser = [Security.Principal.WindowsIdentity]::GetCurrent()
    $principal = New-Object Security.Principal.WindowsPrincipal($currentUser)
    return $principal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)
}

# Create directories
function New-Directories {
    param([string]$DataDir, [string]$InstallDir)

    $dirs = @($DataDir, "$DataDir\data", "$DataDir\log", "$DataDir\run", $InstallDir)

    foreach ($dir in $dirs) {
        if (-not (Test-Path $dir)) {
            Write-Info "Creating directory: $dir"
            New-Item -ItemType Directory -Path $dir -Force | Out-Null
        } else {
            Write-Info "Directory exists: $dir"
        }
    }
}

# Generate random JWT secret
function New-Secret {
    $bytes = New-Object byte[] 32
    $rng = [System.Security.Cryptography.RandomNumberGenerator]::Create()
    $rng.GetBytes($bytes)
    return [Convert]::ToBase64String($bytes) | Select-Object -First 32
}

